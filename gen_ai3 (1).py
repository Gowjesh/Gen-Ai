from transformers import pipeline

# âœ… LOAD GPT-2 PIPELINE
generator = pipeline("text-generation", model="gpt2")

# âœ… FUNCTION TO GENERATE ANIME CONCEPT
def generate_anime_concept(user_idea):
    prompt = f"""
Create a detailed anime concept from the following idea: "{user_idea}"

Include:
1. Title
2. Genre
3. Main Character
4. Setting
5. Plot/Synopsis

Anime Concept:
"""
    result = generator(prompt, max_length=250, temperature=0.8, num_return_sequences=1)[0]['generated_text']
    return result.strip()

# âœ… USER INPUT
user_input = input("ðŸ’¡ Enter your anime idea (e.g. fire user in cyberpunk world):\n> ")
concept = generate_anime_concept(user_input)

